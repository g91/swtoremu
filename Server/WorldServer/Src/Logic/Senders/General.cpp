		void Session::SendGauntletVersion()
		{
			Network::Packet packet(Opcode::SMSG_NOTIFY_GAUNTLET_VERSION, (uint16_t)(mId & 0x0000FFFF), (uint16_t)(mId & 0xFFFF0000));
			packet << (uint32_t)0x11;

			Write(packet);
		}
		//---------------------------------------------------------------------
		void Session::SendScriptErrors(bool pSendErrors)
		{
			Network::Packet packet(Opcode::SMSG_SHOULD_SEND_SCRIPT_ERRORS, (uint16_t)(mId & 0x0000FFFF), (uint16_t)(mId & 0xFFFF0000));
			packet << pSendErrors;

			Write(packet);
		}
